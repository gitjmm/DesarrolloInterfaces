    /*
    MÃ©todo que devuelve true/false al validar un formulario
    */
    private boolean validarFormulario(){
        String ciudad = jTextFieldCiudad.getText();
        if (ciudad == null || "".equals(ciudad)){
            JOptionPane.showMessageDialog(this, "Introduzca ciudad","Error",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        try{
            Integer.parseInt(jTextFieldHabitantes.getText());
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Introduzca numero habitantes","Error",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
    
    private void jButtonValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonValidarActionPerformed
        if (validarFormulario()){
            JOptionPane.showMessageDialog(this, "Formulario Valido");
        }else{
            JOptionPane.showMessageDialog(this, "Formulario NO Valido");
        }
    }//GEN-LAST:event_jButtonValidarActionPerformed

